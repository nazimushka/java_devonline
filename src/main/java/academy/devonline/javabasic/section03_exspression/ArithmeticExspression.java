package academy.devonline.javabasic.section03_exspression;

public class ArithmeticExspression {
    public static void main(String[] args) {
        {
            var a = 5;
            var b = 3;

            System.out.println(a + b);
            System.out.println(a - b);
            System.out.println(a * b);
            System.out.println(a / b);
            System.out.println(a % b);
        }
        {
            var a = 5.;// double
            var b = 3.;// double

            System.out.println(a + b);
            System.out.println(a - b);
            System.out.println(a * b);
            System.out.println(a / b);
            System.out.println(a % b);
        }
    }
}
